ActorBase::ActorBase(void)
{ 
	animationController_ = nullptr;

	animType_ = 0;

	modelId_ = -1;
	ihenModelId_ = -1;
	pos_ = { 0.0f,0.0f,0.0f };
	angle_ = { 0.0f,0.0f,0.0f };
	localAngle_ = { 0.0f,0.0f,0.0f };
	scale_ = { 0.0f,0.0f,0.0f };

	startCapsulePos_ = { 0.0f,0.0f,0.0f };
	endCapsulePos_ = { 0.0f,0.0f,0.0f };
	capsuleRadius_ = 0.0f;

	sphereRadius_ = 0.0f;

	preInputDir_ = { 0.0f,0.0f,0.0f };

	tag_ = TAG::NON;
	isDraw_ = true;
	isGravity_ = false;
	isIhen_ = false;

	moveDir_ = { 0.0f,0.0f,0.0f };
	jumpPow_ = 0.0f;
	isCollision_ = false;
}

ActorBase::~ActorBase(void)
{
}

void ActorBase::Init(void)
{
	// Transform初期化
	InitTransform();

	// 初期化後の個別処理
	InitPost();
}

void ActorBase::Load(void)
{
	// リソースロード
	InitLoad();
}

void ActorBase::LoadEnd(void)
{
	// アニメーションの初期化
	InitAnimation();

	// 初期化
	Init();
}

void ActorBase::Update(void)
{
	if (tag_ == TAG::PLAYER)
	{
		// プレイヤーの遅延回転処理
		DelayRotate();

		// 行列の合成(子, 親と指定すると親⇒子の順に適用される)
		MATRIX mat = MatrixUtility::Multiplication(localAngle_, angle_);

		// 回転行列をモデルに反映
		MV1SetRotationMatrix(modelId_, mat);
		MV1SetRotationMatrix(ihenModelId_, mat);

		// プレイヤーの移動処理
		Move();
	}

	// 重力フラグがあるか？
	if (isGravity_)
	{
		// 重力(加速度を速度に加算していく)
		jumpPow_ -= 0.8f;

		// プレイヤーの座標に移動量(速度、ジャンプ力)を加算する
		pos_.y += jumpPow_;
	}

	// モデルに座標を設定する
	MV1SetPosition(modelId_, pos_);
	MV1SetPosition(ihenModelId_, pos_);
}

